    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        String nama = nmbox.getText();
        int uts = Integer.parseInt(utsbox.getText() );
        int  tm = Integer.parseInt(tmbox.getText() );
        int uas = Integer.parseInt(uasbox.getText() );
        int rata2 = (uts + tm + uas)/3 ;
        
        hasilnm.setText(nama + "");
        hasilrata2.setText(rata2 + ""); 
        
        if (rata2 >= 90) {
            hasilgrade.setText("A");   
            hasil.setText("Lulus");
        }
        else if ((rata2 >= 80 ) || (rata2 <=89)) {
            hasilgrade.setText("B");
            hasil.setText("Lulus");
        }
        else if((rata2 >=70) || (rata2 <= 79)){
            hasilgrade.setText("C");
            hasil.setText("Lulus");
        } else if (rata2 <= 70) {
            hasilgrade.setText("D");
            hasil.setText("Tidak Lulus");
        }
        
       
       
       
    }//GEN-LAST:event_hitungActionPerformed

