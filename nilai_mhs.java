    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        try {
            BufferedWriter out = new BufferedWriter (new FileWriter("HitungNilaiMahasiswa.txt"));
            String save = hasilnm.getText() + '\n'+ hasilrata2.getText() + '\n' + hasilgrade.getText() + '\n' + hasil.getText();
            
            out.write(save);
            JOptionPane.showMessageDialog(null, "Berhasil Disimpan dalam File");
            out.close();
                    } catch (Exception e) {
                        System.err.println("Error: " + e.getMessage());
        }
        
    
    }//GEN-LAST:event_simpanActionPerformed

